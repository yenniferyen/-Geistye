     INDOMILK adalah perusahaan minuman susu yang bermarkas di Jakarta,
 Indonesia. Perusahaan yang bernama lengkap PT. Australia Indonesian 
 Milk Industries ini didirikan pada tahun 1967 sebagai perwujudan 
 penanaman modal asing dan pelopor dalam pembuatan susu  yaitu  susu
 kental manis secara modern di Indonesia. Indomilk awalnya hanya memiliki
 200 karyawan yang terus mengusahakan perkembangan produk - produk
 perusahaan. Indomilk memproduksi susu pasteurisasi merk INDOMILK pada
 tahun 1970 , produk  mentega dengan merk ORCHID BUTTER dan untuk merk
 Golden Churn pada tahun 1971,  produk es krim untuk merk Peter Ice Cream
 pada tahun 1972, serta susu bubuk INDOMILK yang  diproduksi dengan sistem
 toll manufacturing pada tahun 1985. Pada tahun 1986, indomilk meluncurkan
 susu kental manis CAP ENAAK setelah terjadi perubahan status PMDN
 (Penanaman Modal Dalam Negeri). Tahun 1988 susu kental manis produksi
 INDOMILK telah diimpor oleh berbagai negara seperti Malaysia, Singapura,
 Bangladesh, Vietnam, Myanmar, Taiwan, Timur Tengah, Afrika dan Amerika Latin. 
     Hingga tahun 2008, Indomilk menanungi beberapa anak perusahaan dan
