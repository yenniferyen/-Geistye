	  Produk-produk ini telah diekspor ke berbagai negara seperti Singapura, Kamboja, Brunei Darussalam, Philipina, Hongkong, Taiwan,
 Jepang, Korea, Fiji, Nauru, Tonga, Solomon, Samoa, Vanuatu, Papua Nugini, New Zealand, Timor Leste, Cameroon, Malawi, Moroni, Maladewa.
      Visi dari perusahaan susu ini adalah menjadi pemimpin pasar susu di Indonesia dan membangun citra terkemuka di negara lain melalui
 produk-produk berkualitas. Maka untuk memperkuat sinergi dalam badan perusahaan,pada bulan April 2008 dilakukan merger terhadap PT. Australia
 Indonesian Milk Industries (PT. INDOMILK PT. Indomurni Dairy Industries, PT. Ultrindo, PT. INDOLAKTO dan PT. Indoeskrim ke dalam satu payung
 usaha, yaitu PT. INDOLAKTO.Indomilk percaya bahwa langkah ini akan menciptakan nilai tambah perusahaan yang berkelanjutan dengan menghadirkan
 produk PT. INDOLAKTO yang berkualitas di seluruh dunia melalui manajemen usaha yang baik dan pemanfaatan peluang kerjasama dengan
 pihak asing maupun dalam negeri dengan sebaik-baiknya.

